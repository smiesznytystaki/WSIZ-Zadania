tekst = "Python jest super"

# Zerowy znak
print(tekst[0])

# Ostatni znak
print(tekst[-1])

# Co drugi znak zaczynając od zerowego
print(tekst[::2])

# Co trzeci znak zaczynając od pierwszego
print(tekst[1::3])

# Od dziesiątego do ostatniego znak
print(tekst[10:])

# Od końca do początku
print(tekst[::-1])

