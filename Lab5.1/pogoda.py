import temperatura

celsius_to_fahrenheit = temperatura.c_to_f(21)
fahrenheit_to_celsius = temperatura.f_to_c(89)
celsius_to_kelvin = temperatura.c_to_k(35)

print(f"21 stopni Celsjusza to {celsius_to_fahrenheit:.2f} stopni Fahrenheita.")
print(f"89 stopni Fahrenheita to {fahrenheit_to_celsius:.2f} stopni Celsjusza.")
print(f"35 stopni Celsjusza to {celsius_to_kelvin:.2f} stopni Kelwina.")